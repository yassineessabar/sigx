type MockStrategy = (typeof mockStrategies)[0]

function EquityCurveSVG({ data, id }: { data: number[]; id: string }) {
  const width = 320
  const height = 80
  const padding = 4
  const min = Math.min(...data)
  const max = Math.max(...data)
  const range = max - min || 1

  const points = data
    .map((val, i) => {
      const x = padding + (i / (data.length - 1)) * (width - padding * 2)
      const y = height - padding - ((val - min) / range) * (height - padding * 2)
      return `${x},${y}`
    })
    .join(' ')

  const firstX = padding
  const lastX = padding + ((data.length - 1) / (data.length - 1)) * (width - padding * 2)
  const areaPath = `M${firstX},${height} L${points.split(' ').map((p) => p).join(' L')} L${lastX},${height} Z`

  return (
    <svg viewBox={`0 0 ${width} ${height}`} className="w-full h-full" preserveAspectRatio="none">
      <defs>
        <linearGradient id={`grad-${id}`} x1="0" y1="0" x2="0" y2="1">
          <stop offset="0%" stopColor="rgb(74, 222, 128)" stopOpacity="0.15" />
          <stop offset="100%" stopColor="rgb(74, 222, 128)" stopOpacity="0" />
        </linearGradient>
      </defs>
      <path d={areaPath} fill={`url(#grad-${id})`} />
      <polyline
        points={points}
        fill="none"
        stroke="rgb(74, 222, 128)"
        strokeWidth="1.5"
        strokeLinejoin="round"
        strokeLinecap="round"
        vectorEffect="non-scaling-stroke"
      />
    </svg>
  )
}

// Check if an ID is a real UUID (from the database) vs a mock short ID
function isRealId(id: string): boolean {
  return id.length > 10 && id.includes('-')
}

