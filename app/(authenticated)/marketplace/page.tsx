// Generate realistic equity curve points
function generateEquityCurve(seed: number, points: number = 40): number[] {
  const curve: number[] = [100]
  let val = 100
  for (let i = 1; i < points; i++) {
    const noise = Math.sin(seed * i * 0.7) * 2 + Math.cos(seed * i * 0.3) * 1.5
    val += noise * 0.4 + 0.3
    curve.push(val)
  }
  return curve
}

